# Examples of different Python data types

# Number types
integer_number = 25
float_number = 36.6
complex_number = 3 + 4j

# String
name = "Oscar"

# Boolean
is_student = True

# List
fruits = ["apple", "banana", "cherry"]

# Tuple
coordinates = (10, 20)

# Set
unique_numbers = {1, 2, 3, 3, 4}

# Dictionary
person = {"name": "Oscar", "age": 25, "is_student": True}

print("Integer:", integer_number, type(integer_number))
print("Float:", float_number, type(float_number))
print("Complex:", complex_number, type(complex_number))
print("String:", name, type(name))
print("Boolean:", is_student, type(is_student))
print("List:", fruits, type(fruits))
print("Tuple:", coordinates, type(coordinates))
print("Set:", unique_numbers, type(unique_numbers))
print("Dictionary:", person, type(person))


